// Mock implementation of useTranslations
export function useTranslations(namespace: string) {
  return (key: string, params?: Record<string, string | number>) => {
    // Get the translations for the namespace
    // Use index assertion to tell TypeScript that namespace is a valid key
    const namespaceTranslations =
      translations[namespace as keyof typeof translations] || {}

    // Handle nested keys with dot notation
    const keyParts = key.split('.')
    let translationValue: any = namespaceTranslations

    // Traverse the nested object structure
    for (const part of keyParts) {
      if (translationValue && typeof translationValue === 'object') {
        translationValue =
          translationValue[part as keyof typeof translationValue]
      } else {
        translationValue = undefined
        break
      }
    }

    // If the value is undefined, return the key
    if (translationValue === undefined) {
      return key
    }

    // If the value is a string, handle parameter replacement
    if (typeof translationValue === 'string') {
      let translation = translationValue

      // Replace parameters in the translation if provided
      if (params) {
        Object.entries(params).forEach(([paramKey, paramValue]) => {
          translation = translation.replace(`{${paramKey}}`, String(paramValue))
        })
      }

      return translation
    }

    // If the value is an object or array, return it directly
    return translationValue
  }
}
